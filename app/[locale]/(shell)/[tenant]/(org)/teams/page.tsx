import { BasketballTeamsTable } from "@/components/sections/shell/sports/basketball/teams-table";
import { SoccerTeamsTable } from "@/components/sections/shell/sports/soccer/teams-table";
import { preloadQuery } from "convex/nextjs";
import { api } from "@/convex/_generated/api";

interface TeamsPageProps {
  params: Promise<{
    tenant: string;
  }>;
}

export default async function TeamsPage({ params }: TeamsPageProps) {
  // Explore the possibility of using the provider instead of passing the tenant directly
  const { tenant } = await params;

  // TODO: Replace with actual sportType detection from league data
  const sportType = "basketball";

  if (sportType === "basketball") {
    const preloadedTeams = await preloadQuery(api.clubs.listByLeague, {
      orgSlug: tenant,
    });
    return (
      <BasketballTeamsTable preloadedTeams={preloadedTeams} orgSlug={tenant} />
    );
  }

  if (sportType === "soccer") {
    const preloadedTeams = await preloadQuery(api.clubs.listByLeague, {
      orgSlug: tenant,
    });
    return <SoccerTeamsTable preloadedData={preloadedTeams} orgSlug={tenant} />;
  }

  return <div>Unsupported sport type</div>;
}
